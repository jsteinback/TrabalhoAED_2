package Geral;

public class Letra {

	private String letra;
	private int peso;

	public Letra() {

	}

	public Letra(String letra, int peso) {
		this.letra = letra;
		this.peso = peso;
	}

	public String getLetra() {
		return letra;
	}

	public void setLetra(String letra) {
		this.letra = letra;
	}

	public int getPeso() {
		return peso;
	}

	public void setPeso(int peso) {
		this.peso = peso;
	}

}
